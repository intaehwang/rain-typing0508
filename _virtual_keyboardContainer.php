<script>
$(function() {

	$(".key-set .key.shift").click(function(event){
		if($(this).closest('.key-set').hasClass('lang-ko')) {
			$('.lang-ko2').show();
			$('.key-set').not('.lang-ko2').hide();
		} else if($(this).closest('.key-set').hasClass('lang-ko2')) {
			$('.lang-ko').show();
			$('.key-set').not('.lang-ko').hide();
		} else if($(this).closest('.key-set').hasClass('lang-eng')) {
			$('.lang-eng2').show();
			$('.key-set').not('.lang-eng2').hide();
		} else if($(this).closest('.key-set').hasClass('lang-eng2')) {
			$('.lang-eng').show();
			$('.key-set').not('.lang-eng').hide();
		}
	});
	$('.key-set .key.ko').click(function(event){
		$('.lang-ko').show();
		$('.key-set').not('.lang-ko').hide();
	});
	$('.key-set .key.eng').click(function(event){
		$('.key-set.lang-eng').show();
		$('.key-set').not('.lang-eng').hide();
	});



	$("#virtual-keyboardContainer .key").on("touchstart", function(e){
		var key = $(this).attr('value');
		if(key=='backspace'){
			var keylength = $('#text-input').val().length;
			var result1 = $('#text-input').val().substring(0, keylength-1);
			$('#text-input').val(result1);
		}else{
			$('#text-input').val($('#text-input').val()+key);
		}
    });

	document.documentElement.addEventListener('touchstart', function (event) {
		if (event.touches.length > 1) {
			event.preventDefault();
		} 
	}, false);

	var lastTouchEnd = 0;

	document.documentElement.addEventListener('touchend', function (event) {
		var now = (new Date()).getTime();
		if (now - lastTouchEnd <= 300) {
			event.preventDefault();
		} lastTouchEnd = now;
	}, false);
    
});
</script>